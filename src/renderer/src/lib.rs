struct Camera {
    eye: Vec3,
    target: Vec3,
    up: Vec3,
    aspect: f32,
    fovy: f32,
    znear: f32,
    zfar: f32,
}

impl Camera {
    fn build_view_projection_matrix(&self) -> Mat4 {
        // 1.
        let view = Mat4::look_at_rh(self.eye, self.target, self.up);
        // 2.
        let proj =
            Mat4::perspective_rh_gl(self.fovy.to_radians(), self.aspect, self.znear, self.zfar);

        // 3.

        let wgpu_matrix = Mat4::from_cols(
            // TODO this was a const in the tut, need to verify it isn't constantly recreated
            Vec4::new(1.0, 0.0, 0.0, 0.0),
            Vec4::new(0.0, 1.0, 0.0, 0.0),
            Vec4::new(0.0, 0.0, 0.5, 0.5),
            Vec4::new(0.0, 0.0, 0.0, 1.0),
        );

        return wgpu_matrix * proj * view;
    }
}

