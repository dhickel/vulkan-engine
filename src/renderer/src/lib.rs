#[repr(C)]
// This is so we can store this in a buffer
#[derive(Debug, Copy, Clone, bytemuck::Pod, bytemuck::Zeroable)]
struct CameraUniform {
    // We can't use cgmath with bytemuck directly, so we'll have
    // to convert the Matrix4 into a 4x4 f32 array
    view_proj: [[f32; 4]; 4],
}

impl CameraUniform {
    fn new() -> Self {
        Self {
            view_proj: Mat4::identity().to_cols_array_2d(),
        }
    }

    fn update_view_proj(&mut self, camera: &Camera) {
        self.view_proj = camera.build_view_projection_matrix().to_cols_array_2d();
    }
}

struct CameraController {
    speed: f32,
    directions : [bool; 4],
    listening_for: [KeyCode; 4],
}

impl KeyboardListener for CameraController {
    fn listener_type(&self) -> ListenerType {
        ListenerType::GameInput
    }

    fn listener_id(&self) -> u32 {
        1
    }

    fn listener_for(&self, key: KeyCode) -> bool {
        self.listening_for.contains(&key)
    }

    fn broadcast(&mut self, key: KeyCode, pressed: bool, modifiers: &HashSet<Modifiers>) {
        println!("boardcasting: {:?}", key);
        match key {
            KeyCode::KeyW => self.directions[0] = true,
            KeyCode::KeyS => self.directions[1] = true,
            KeyCode::KeyA => self.directions[2]= true,
            KeyCode::KeyD => self.directions[3] = true,
            _ => {}
        }
    }
}

impl CameraController {
    fn new(speed: f32) -> Self {
        Self {
            speed,
            directions: [false; 4],
            listening_for: [KeyCode::KeyW, KeyCode::KeyA, KeyCode::KeyS, KeyCode::KeyD],
        }
    }

    fn update_camera(&mut self, camera: &mut Camera) {
        let forward = camera.target - camera.eye;
        let forward_norm = forward.normalize();
        let forward_mag = forward.length();

        // Prevents glitching when the camera gets too close to the
        // center of the scene.
        if self.directions[0] && forward_mag > self.speed {
            camera.eye += forward_norm * self.speed;
        }
        if self.directions[1] {
            camera.eye -= forward_norm * self.speed;
        }

        let right = forward_norm.cross(camera.up);

        // Redo radius calc in case the forward/backward is pressed.
        let forward = camera.target - camera.eye;
        let forward_mag = forward.length();

        if  self.directions[3]{
            // Rescale the distance between the target and the eye so
            // that it doesn't change. The eye, therefore, still
            // lies on the circle made by the target and eye.
            camera.eye = camera.target - (forward + right * self.speed).normalize() * forward_mag;
        }
        if self.directions[2] {
            camera.eye = camera.target - (forward - right * self.speed).normalize() * forward_mag;
        }

        self.directions.fill(false);
    }

}
